class HelloWorld {
  public static void main(String[] args) {
    int number1 = 12;
    int number2 = 15;

    // is equal to
    System.out.println(number1 == number2);

    // is not equal to
    System.out.println(number1 != number2);

    // is greater to
    System.out.println(number1 > number2);

    // is less to
    System.out.println(number1 < number2);

    // is greater than or equal to
    System.out.println(number1 >= number2);

    // is less than or equal to
    System.out.println(number1 <= number2);
  }
}
